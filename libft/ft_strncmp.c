#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*s1_uc;
	unsigned char	*s2_uc;

	s1_uc = (unsigned char *)s1;
	s2_uc = (unsigned char *)s2;
	i = 0;
	while (s1_uc[i] && s2_uc[i] && i < n)
	{
		if (s1_uc[i] != s2_uc[i])
			return (s1_uc[i] - s2_uc[i]);
		i++;
	}
	if ((s1_uc[i] != '\0' || s2_uc[i] != '\0') && i < n)
		return (s1_uc[i] - s2_uc[i]);
	return (0);
}
